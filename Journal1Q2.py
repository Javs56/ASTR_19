#Write a Python that prints the sum of two floating point numbers, the difference between two integers, 
#and the product of a floating point number and an integer. In each case, have the program print out the 
#data type of the resulting answer.

a = 2.3
b = 7.6
c = a + b
print(a, '+', b, 'is', c, '\n and its data type is')
print(type(c), '\n')

d = 5
e = 10
f = d-e
print(d, '-', e, 'is', f, '\n and its data type is')
print(type(f), '\n')

g = e*b
